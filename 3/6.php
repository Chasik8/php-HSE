<?php

/**
 * Задание 6.
 * Заменяет отрицательные элементы массива C (C_1...C_15)
 * на сумму S = Σ(C_i^2) для i с 5 по 10.
 *
 * @param array $C Массив C (C_1, ..., C_15). Модифицируется по ссылке.
 * @return float|string Сумма S или сообщение об ошибке.
 */
function processArrayTask6(array &$C): float|string
{
    // 1. Валидация входных данных
    if (count($C) !== 15) {
        return "Ошибка: Входной массив C должен содержать ровно 15 элементов.";
    }

    // 2. Рассчитываем S = Σ(C_i^2) для i с 5 по 10.
    // В PHP-массивах с 0-индексацией это элементы с индексами [4] по [9].
    $S = 0.0;
    for ($i = 4; $i <= 9; $i++) {
        if (!is_numeric($C[$i])) {
            return "Ошибка: Элемент C_" . ($i + 1) . " не является числом.";
        }
        $S += $C[$i] ** 2; // pow($C[$i], 2)
    }

    // 3. Заменяем отрицательные элементы во *всем* массиве
    foreach ($C as $j => $value) {
        if (!is_numeric($value)) {
            return "Ошибка: Элемент C_" . ($j + 1) . " не является числом.";
        }
        if ($value < 0) {
            $C[$j] = $S;
        }
    }

    // Возвращаем S для целей логирования
    return $S;
}

// --- Набор тестовых случаев ---
$test_cases = [
    // Тест 1: Смешанные значения
    [
        'C' => [
            10, -5, 2, 1,      // C_1...C_4
            2, 3, 1, -1, 0, 5, // C_5...C_10 (S = 2²+3²+1²+(-1)²+0²+5² = 4+9+1+1+0+25 = 40)
            -10, 8, -1.5, 0, 20 // C_11...C_15
        ]
    ],
    // Тест 2: Нет отрицательных элементов
    [
        'C' => [
            1, 2, 3, 4,
            1, 1, 1, 1, 1, 1, // S = 1*6 = 6
            5, 6, 7, 8, 9
        ]
    ],
    // Тест 3: Ошибка валидации
    [
        'C' => [1, 2, 3]
    ]
];

// --- Выполнение и вывод результатов ---
echo "РЕЗУЛЬТАТЫ ВЫПОЛНЕНИЯ ЗАДАНИЯ 6\n";
echo "==================================\n";

foreach ($test_cases as $index => $data) {
    echo "Тест " . ($index + 1) . ":\n";

    // Копируем массив, чтобы сохранить оригинал для вывода
    $C_original = $data['C'];
    $C_processed = $data['C'];

    echo "Исходный массив C: [" . implode(", ", $C_original) . "]\n";

    $result = processArrayTask6($C_processed);

    if (is_numeric($result)) {
        echo "Результат:\n";
        echo "  Рассчитанная сумма S (для C_5..C_10) = " . $result . "\n";
        echo "  Модифицированный массив C: [" . implode(", ", $C_processed) . "]\n";
    } else {
        echo "Результат: " . $result . "\n";
    }
    echo "----------------------------------\n";
}

